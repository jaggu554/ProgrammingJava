
public class JavaMathex {

	public static void main(String[] args) {
		double x=24;
		double y =4;
		System.out.println("Max num of x and y is:"+Math.max(x, y));
		System.out.println("Square root of y is :"+Math.sqrt(x));
		System.out.println("power of x and y is :"+Math.pow(x, y));
		System.out.println("Logarithm of x is:"+Math.log(y));
		System.out.println("Logarithm of y is :"+Math.log(x));
		System.out.println("exp of x is :"+Math.exp(x));

	}

}
